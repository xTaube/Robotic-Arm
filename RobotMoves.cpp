//funkcja skleja wszystkie ruchy do jednej QList'y wed≈Çug wagi skrzynek
QList <unsigned long> makeSortKeyTab(QList<Chest *> ch)
{
    QList <unsigned long> move;
    QList <unsigned long> pom1;
    int pom2;
    for(int i = 0 ; i<4 ; i++){
        if(i == 0){
            if(ch[i]->chestNumber == 1) pom1 = firstIsHeaviest();
            if(ch[i]->chestNumber == 2) pom1 = secondIsHeaviest();
            if(ch[i]->chestNumber == 3) pom1 = thirdIsHeaviest();
            if(ch[i]->chestNumber == 4) pom1 = fourthIsHeaviest();
        }
        if(i == 1){
            if(ch[i]->chestNumber == 1) pom1 = firstIsSecondHeaviest();
            if(ch[i]->chestNumber == 2) pom1 = secondIsSecondHeaviest();
            if(ch[i]->chestNumber == 3) pom1 = thirdIsSecondHeaviest();
            if(ch[i]->chestNumber == 4) pom1 = fourthIsSecondHeaviest();
        }
        if(i == 2){
            if(ch[i]->chestNumber == 1) pom1 = firstIsThirdHeaviest();
            if(ch[i]->chestNumber == 2) pom1 = secondIsThirdHeaviest();
            if(ch[i]->chestNumber == 3) pom1 = thirdIsThirdHeaviest();
            if(ch[i]->chestNumber == 4) pom1 = fourthIsThirdHeaviest();
        }
        if(i == 3){
            if(ch[i]->chestNumber == 1) pom1 = firstIsLightest();
            if(ch[i]->chestNumber == 2) pom1 = secondIsLightest();
            if(ch[i]->chestNumber == 3) pom1 = thirdIsLightest();
            if(ch[i]->chestNumber == 4) pom1 = fourthIsLightest();
        }
        for(int j = 0 ; j<pom1.size() ; j++){
            pom2 = pom1[j];
            move.push_back(pom2);
        }
        pom1.clear();
    }

    return move;
}
